var_to_lbl <- function(df, col) {
  
  df_lbl <- df %>%
    mutate({{col}} := str_replace_all({{col}}, "\\.", ' ')) %>% 
    mutate({{col}} := case_when(
      str_detect({{col}}, regex("AGEENTRY", ignore_case = TRUE)) ~ "Age",
      str_detect({{col}}, regex("GENDER", ignore_case = TRUE)) ~ "Sex",
      str_detect({{col}}, regex("VMIN", ignore_case = TRUE)) ~ "Ethnicity",
      str_detect({{col}}, regex("FIQ", ignore_case = TRUE)) ~ "IQ",
      str_detect({{col}}, regex("EDUC_NUM", ignore_case = TRUE)) ~ "Education (years)",
      str_detect({{col}}, regex("NEET", ignore_case = TRUE)) ~ "Employment",
      str_detect({{col}}, regex("INDPT", ignore_case = TRUE)) ~ "Housing",
      str_detect({{col}}, regex("REL", ignore_case = TRUE)) ~ "Relationship",
      str_detect({{col}}, regex("HOLLTOTP", ignore_case = TRUE)) ~ "Socioeconomic status",
      str_detect({{col}}, regex("PAS_tot2", ignore_case = TRUE)) ~ "Premorbid adjustment",
      str_detect({{col}}, regex("AGEONSET", ignore_case = TRUE)) ~ "Onset",
      str_detect({{col}}, regex("DUPONSET", ignore_case = TRUE)) ~ "Duration of untreated psychosis",
      str_detect({{col}}, regex("DUI", ignore_case = TRUE)) ~ "Duration of untreated illness",
      str_detect({{col}}, regex("MODE", ignore_case = TRUE)) ~ "Mode of onset",
      str_detect({{col}}, regex("CAYRCONV", ignore_case = TRUE)) ~ "Clinical high-risk transition",
      str_detect({{col}}, regex("IP_OP", ignore_case = TRUE)) ~ "Hospitalization",
      str_detect({{col}}, regex("DX_B2", ignore_case = TRUE)) ~ "Schizophrenia-spectrum disorder",
      str_detect({{col}}, regex("SUD", ignore_case = TRUE)) ~ "Substance use disorder",
      str_detect({{col}}, regex("VERBM_Z", ignore_case = TRUE)) ~ "Verbal memory", 
      str_detect({{col}}, regex("WM_Z", ignore_case = TRUE)) ~ "Working memory",
      str_detect({{col}}, regex("EF_Z", ignore_case = TRUE)) ~ "Executive functioning",
      str_detect({{col}}, regex("SOP_Z", ignore_case = TRUE)) ~ "Speed of processing",
      str_detect({{col}}, regex("VISM_Z", ignore_case = TRUE)) ~ "Visual memory",
      str_detect({{col}}, regex("VA_Z", ignore_case = TRUE)) ~ "Visual attention",
      str_detect({{col}}, regex("SC_Z", ignore_case = TRUE)) ~ "Social cognition",
      str_detect({{col}}, regex("TXM0CO", ignore_case = TRUE)) ~ "Court-ordered treatment",
      str_detect({{col}}, regex("EDUC", ignore_case = TRUE)) ~ "Education",
      str_detect({{col}}, regex("SOFAS", ignore_case = TRUE)) ~ "Functioning",
      str_detect({{col}}, regex("SAPS", ignore_case = TRUE)) ~ "Positive symptoms",
      str_detect({{col}}, regex("SANS", ignore_case = TRUE)) ~ "Negative symptoms",
      str_detect({{col}}, regex("HAS", ignore_case = TRUE)) ~ "Anxiety symptoms",
      str_detect({{col}}, regex("CDS", ignore_case = TRUE)) ~ "Depressive symptoms",
      str_detect({{col}}, regex("YMRS", ignore_case = TRUE)) ~ "Mania symptoms",
      str_detect({{col}}, regex("CD8", ignore_case = TRUE)) ~ "Suicidal ideation",
      str_detect({{col}}, regex("SUMD1", ignore_case = TRUE)) ~ "Unawareness",
      str_detect({{col}}, regex("SUMD2", ignore_case = TRUE)) ~ "Unawareness of positive symptoms",
      str_detect({{col}}, regex("SUMD3", ignore_case = TRUE)) ~ "Unawareness of negative symptoms",
      str_detect({{col}}, regex("CPZ", ignore_case = TRUE)) ~ "Chlorpromazine equivalent",
      str_detect({{col}}, regex("COMP", ignore_case = TRUE)) ~ "Adherence (%)",
      str_detect({{col}}, regex("RESP50_SAP", ignore_case = TRUE)) ~ "Early response (PS)",
      str_detect({{col}}, regex("RESP50_SAN", ignore_case = TRUE)) ~ "Early response (NS)",
      str_detect({{col}}, regex("RESP50_tot", ignore_case = TRUE)) ~ "Early response (Total)",
      # str_detect({{col}}, regex("PSR_1ST", ignore_case = TRUE)) ~ "Time to response (PS)", 
      # str_detect({{col}}, regex("NSR_1ST", ignore_case = TRUE)) ~ "Time to response (NS)",
      # str_detect({{col}}, regex("JSR_1ST", ignore_case = TRUE)) ~ "Time to response (total)",
      # str_detect({{col}}, regex("PSR_BY3", ignore_case = TRUE)) ~ "Early response (PS)", 
      # str_detect({{col}}, regex("NSR_BY3", ignore_case = TRUE)) ~ "Early response (NS)",
      # str_detect({{col}}, regex("JSR_BY3", ignore_case = TRUE)) ~ "Early response (total)", 
      str_detect({{col}}, regex("PSR_AT3", ignore_case = TRUE)) ~ "Early remission of PS", 
      str_detect({{col}}, regex("NSR_AT3", ignore_case = TRUE)) ~ "Early remission of NS",
      str_detect({{col}}, regex("JSR_AT3", ignore_case = TRUE)) ~ "Early clinical remission", 
      str_detect({{col}}, regex("PSR_AT6", ignore_case = TRUE)) ~ "Early remission of PS (mth6)", 
      str_detect({{col}}, regex("NSR_AT6", ignore_case = TRUE)) ~ "Early remission of NS (mth6)",
      str_detect({{col}}, regex("JSR_AT6", ignore_case = TRUE)) ~ "Early clinical remission (mth6)", 
      str_detect({{col}}, regex("PSR_24C", ignore_case = TRUE)) ~ "Time in remission of positive symptoms",
      str_detect({{col}}, regex("NSR_24C", ignore_case = TRUE)) ~ "Time in remission of negative symptoms",
      str_detect({{col}}, regex("JSR_24C", ignore_case = TRUE)) ~ "Time in remission of total symptoms",
      str_detect({{col}}, regex("PSR", ignore_case = TRUE)) ~ "Remission of positive symptoms", 
      str_detect({{col}}, regex("NSR", ignore_case = TRUE)) ~ "Remission of negative symptoms", 
      str_detect({{col}}, regex("JSR", ignore_case = TRUE)) ~ "Clinical remission", 
      str_detect({{col}}, regex("FR", ignore_case = TRUE)) ~ "Functional remission",
      str_detect({{col}}, regex("RECOV", ignore_case = TRUE)) ~ "Recovery",
      .default = {{col}}
    ))
  return(df_lbl)
}
